# X-Ray Image Dataset

This repository contains an x-ray image dataset for fracture detection.

## Dataset Details

- **Fracture Images:** [Link to Fracture Images](https://drive.google.com/drive/folders/1jzHQfauFoia1qdqYMUtjjVxmrKnWPqgF)
- **Normal Images:** [Link to Normal Images](https://drive.google.com/drive/folders/1iGV1v3P_VCWtSyu90ktm-eodhZcjfB7t)

## Usage

You can use the images in this dataset for training and evaluating your fracture detection models.

Please remember to follow ethical guidelines and data protection regulations when using this dataset.
